    /**
     * Este método sirve para mostrar un mensaje en la pantalla.
     *
     * @param mensaje, es el mensaje que se debe mostrar en la pantalla.
     */
    private void mostrarMensajeEnPantalla(String mensaje) {
        JOptionPane.showMessageDialog(null, mensaje, "Cuidado", 1);
    }

    /**
     * Este método se encarga de dejar todo como estaba para poder cerrar la
     * ventana.
     */
    private void cerrarEstaVentana() {
        borrarDatosDeCampos();
        this.dispose();
    }

    /**
     * Este método se encarga de limpiar los campos de la ventana.
     */
    private void borrarDatosDeCampos() {
        this.txtNombreEmpleado.setText("");
        this.txtNombreEmpleado.requestFocus();
        this.txtIdEmpleado.setText("");
        this.txtDireccionEmpleado.setText("");
        this.txtTelefonoEmpleado.setText("");
        this.txtCorreoEmpleado.setText("");
        this.txtSueldoEmpleado.setText("");
        this.grupoBotones.clearSelection();
    }

