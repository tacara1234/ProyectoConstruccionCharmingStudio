    private void btnGuardarEmpleadoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarEmpleadoActionPerformed
        //Primero obtenemos los datos del empleado
        //y las guardamos en las siguientes variables:

        int id;
        if (!this.txtIdEmpleado.getText().isEmpty()) {
            id = Integer.parseInt(this.txtIdEmpleado.getText());
        }else{
            id =0;
        }

        float desempenio = 0;

        if (this.cbDesempenio1.isSelected() == true) {
            desempenio = 1;
        }
        if (this.cbDesempenio2.isSelected() == true) {
            desempenio = 2;
        }
        if (this.cbDesempenio3.isSelected() == true) {
            desempenio = 3;
        }
        if (this.cbDesempenio4.isSelected() == true) {
            desempenio = 4;
        }
        if (this.cbDesempenio5.isSelected() == true) {
            desempenio = 5;
        }

        String nombre = this.txtNombreEmpleado.getText();
        String direccion = this.txtDireccionEmpleado.getText();
        String telefono = this.txtTelefonoEmpleado.getText();
        String correo = this.txtCorreoEmpleado.getText();
        float sueldo = Float.parseFloat(this.txtSueldoEmpleado.getText());

        //Creamos la variable temporal de empleado..
        Empleado empleadoTemporal = new Empleado(id, nombre, direccion,
                telefono, correo, desempenio, sueldo);

        //creamos su controlador:
        ControladorEmpleado ctrlEmpleado = new ControladorEmpleado();

        try {
            if (seModificaraEmpleado) {
                boolean seModificoEmpleado = ctrlEmpleado.modificar(empleadoTemporal);
                if (seModificoEmpleado) {
                    mostrarMensajeEnPantalla("Empleado Modificado");
                } else {
                    mostrarMensajeEnPantalla("Empleado No Modificado");
                }
                //lo devolvemos a su estado inicial.
                seModificaraEmpleado = false;
            } else {
                boolean seAgregoEmpleado = ctrlEmpleado.agregar(empleadoTemporal);
                if (seAgregoEmpleado) {
                    mostrarMensajeEnPantalla("Empleado Agregado");
                } else {
                    mostrarMensajeEnPantalla("Empleado No Agregado");
                }

            }
        } catch (SQLException ex) {
            mostrarMensajeEnPantalla("Hubo un error con la "
                    + "base de datos. " + ex.getLocalizedMessage());
        }
        //borramos los datos:
        borrarDatosDeCampos();


    }//GEN-LAST:event_btnGuardarEmpleadoActionPerformed

