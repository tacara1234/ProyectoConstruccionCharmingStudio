    private void mostrarMensajeEnPantalla(String mensaje) {
        JOptionPane.showMessageDialog(null, mensaje, "Cuidado", 1);
    }

    private void borrarDatosDeCampos() {
        this.txtIdCliente.setText("");
        this.txtCorreoCliente.setText("");
        this.txtDireccionCliente.setText("");
        this.txtNombreCliente.setText("");
        this.txtTelefonoCliente.setText("");
    }

    private void cerrarEstaVentana() {
        borrarDatosDeCampos();
        this.dispose();
    }

