    private void btnGuardarClienteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarClienteActionPerformed
        //Primero obtenemos los datos del cliente
        //y las guardamos en las siguientes variables:
        //Primero obtenemos los datos del cliente
        //y las guardamos en las siguientes variables:
        int id;
        if (!this.txtIdCliente.getText().isEmpty()) {
            id = Integer.parseInt(this.txtIdCliente.getText());
        }else{
            id = 0;
        }
        
        String nombre = this.txtNombreCliente.getText();
        String direccion = this.txtDireccionCliente.getText();
        String telefono = this.txtTelefonoCliente.getText();
        String correo = this.txtCorreoCliente.getText();
                
        //Creamos la variable temporal de cliente..
        Cliente tempCliente = new Cliente(id,nombre, direccion, telefono, correo);
        ControladorCliente ctrlCliente = new ControladorCliente();
        
        try {
            if (!seModificaraCliente) {
                boolean seAgregoCliente = ctrlCliente.agregar(tempCliente);
                 if (seAgregoCliente) mostrarMensajeEnPantalla("Cliente Agregado");
                 else mostrarMensajeEnPantalla("Cliente No Agregado");
                
            }else{
                boolean seModificoCliente = ctrlCliente.modificar(tempCliente);
                 if (seModificoCliente) mostrarMensajeEnPantalla("Cliente Modificado");
                 else  mostrarMensajeEnPantalla("Cliente No Modificado");
                 //lo devolvemos a su estado de inicio.
                seModificaraCliente = false;
            }
        } catch (SQLException ex) {
            mostrarMensajeEnPantalla("Hubo un error con la "
                    + "base de datos. " + ex.getLocalizedMessage());
        }

        borrarDatosDeCampos();
    }//GEN-LAST:event_btnGuardarClienteActionPerformed

